func third() {
	trimmed := strings.Trim(thirdData, "\n")
	lines := strings.Split(trimmed, "\n")

	totalLines := len(lines)
	log.Println(totalLines)

	grid := [][]bool{}

	for _, line := range lines {
		l := strings.Split(line, "")
		r := []bool{}

		for _, c := range l {
			if c == "." {
				r = append(r, false)
			} else {
				r = append(r, true)
			}
		}

		grid = append(grid, r)
	}

	empty := make([]struct{}, totalLines)

	lastR := 0
	treeCount := 0
	for i := range empty {
		if i+1 == totalLines {
			break
		}
		lastR += 3

		sec := (lastR) % 32
		log.Println(i+1, sec)
		if grid[i+1][sec+1] {
			treeCount++
		}
	}

	log.Println(treeCount)
}

